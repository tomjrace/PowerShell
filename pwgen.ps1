$symbols = '!','@','#','$','%','^','&','*'
$cacheFile = "$env:USERPROFILE\.pwgen_seed_cache.txt"

function Get-DailyRandomSeed {
    $today = (Get-Date).Date.ToString("yyyy-MM-dd")
    if (Test-Path $cacheFile) {
        $cached = Get-Content $cacheFile -Raw | ConvertFrom-Json -ErrorAction SilentlyContinue
        if ($cached -and $cached.date -eq $today) {
            return $cached.seed
        }
    }

    Write-Host "Fetching fresh entropy from random.org..." -ForegroundColor Cyan
    try {
        $url = "https://www.random.org/integers/?num=1&min=1&max=1000000000&col=1&base=10&format=plain&rnd=new"
        $webSeed = (Invoke-WebRequest -Uri $url -UseBasicParsing -TimeoutSec 10).Content.Trim()
        $seed = [int]$webSeed
        $cache = [PSCustomObject]@{ date = $today; seed = $seed } | ConvertTo-Json -Compress
        $cache | Out-File -FilePath $cacheFile -Encoding utf8 -Force
        return $seed
    }
    catch {
        Write-Warning "random.org unreachable — falling back to local RNG"
        return Get-Random -Maximum ([int]::MaxValue)
    }
}

# Seed once using random.org + time
$dailySeed = Get-DailyRandomSeed
$timeSeed  = (Get-Date).Ticks
$finalSeed = ($dailySeed -bxor $timeSeed) -band 0x7FFFFFFF
$null = Get-Random -SetSeed $finalSeed

