$results = foreach ($i in 1..$Count) {
    $verb        = $verbings | Get-Random
    $animalRaw   = $animals  | Get-Random
    $animalClean = (Get-Culture).TextInfo.ToTitleCase($animalRaw.ToLower()) -replace '\s',''
    $digits      = Get-Random -Minimum 0 -Maximum 100 | ForEach-Object { $_.ToString("00") }
    $symbol      = $symbols  | Get-Random

    "$verb$animalClean$digits$symbol"
}

